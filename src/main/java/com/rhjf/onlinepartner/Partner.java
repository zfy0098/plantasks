package com.rhjf.onlinepartner;

public class Partner {
	
	private Integer id;
	private String partnerName;
	private String agentid;
	private String agentName;
	private String agentNO;
	private String bankID;
	private String t0rate;
	private String t1rate;
	private String agentT0rate;
	private String agentT1rate;
	private String ratio;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getPartnerName() {
		return partnerName;
	}
	public void setPartnerName(String partnerName) {
		this.partnerName = partnerName;
	}
	public String getAgentid() {
		return agentid;
	}
	public void setAgentid(String agentid) {
		this.agentid = agentid;
	}
	public String getAgentName() {
		return agentName;
	}
	public void setAgentName(String agentName) {
		this.agentName = agentName;
	}
	public String getAgentNO() {
		return agentNO;
	}
	public void setAgentNO(String agentNO) {
		this.agentNO = agentNO;
	}
	public String getBankID() {
		return bankID;
	}
	public void setBankID(String bankID) {
		this.bankID = bankID;
	}
	public String getT0rate() {
		return t0rate;
	}
	public void setT0rate(String t0rate) {
		this.t0rate = t0rate;
	}
	public String getT1rate() {
		return t1rate;
	}
	public void setT1rate(String t1rate) {
		this.t1rate = t1rate;
	}
	public String getAgentT0rate() {
		return agentT0rate;
	}
	public void setAgentT0rate(String agentT0rate) {
		this.agentT0rate = agentT0rate;
	}
	public String getAgentT1rate() {
		return agentT1rate;
	}
	public void setAgentT1rate(String agentT1rate) {
		this.agentT1rate = agentT1rate;
	}
	public String getRatio() {
		return ratio;
	}
	public void setRatio(String ratio) {
		this.ratio = ratio;
	}
}
